numero = int(input( "Digite um número e teste sua sorte: "))

if numero <= 3 :
    print( " Não se preocupe com o futuro, ele já está rindo de você também" )

elif 3 < numero < 7 :
    print( " Sorte do dia: hoje ninguém vai te pedir dinheiro emprestado! " )

else:
    print( " O importante não é vencer, é fazer parecer que tentou! " )